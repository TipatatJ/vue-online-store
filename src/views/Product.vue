<template>
  <div>
    <section class="wrapper">
      <div class="flex-col">
        <img
          class="flex-col--2"
          :src="imagePath(product)"
          alt="product image"
        />
        <div class="flex-col--2">
          <h2>{{ product.name }}</h2>
          <p>Prize: ${{ product.price }}</p>
          <p>Size: {{ product.size }}</p>
          <p>Color: {{ product.color }}</p>
          <p>
            <em>{{ product.quantity }} left in stock</em>
          </p>
          <h3>Details</h3>
          <ul>
            <li>Material: {{ product.details.material }}</li>
            <li>Fit: {{ product.details.fit }}</li>
            <li>Maintenance: {{ product.details.maintenance }}</li>
            <li v-if="product.details.additional">
              Additional information: {{ product.details.additional }}
            </li>
          </ul>
        </div>
      </div>
    </section>
  </div>
</template>

<script>
export default {
  name: "product",
  data() {
    return {
      product: this.$store.getters.product(this.$route.params.id)
    };
  },
  methods: {
    imagePath(product) {
      return require(`../assets/img/products/${product.images[0]}`);
    }
  }
};
</script>

